package binarytree;

public class Prac {

	private int data;
	private int cata;
	private String c;
	
	public Prac() {
		
	}

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public int getCata() {
		return cata;
	}

	public void setCata(int cata) {
		this.cata = cata;
	}

	public String getC() {
		return c;
	}

	public void setC(String c) {
		this.c = c;
	}
	
	
}
